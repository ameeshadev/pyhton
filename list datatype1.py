# list datatype
list1=["Honey","Mahir","Erfan","Imran","Ameesha"]

# removing list
list1.remove("Ameesha")
print(list1)
# by using pop() method we delete or remove the list
list1.pop(3)
print(list1)
# for deleting list we use DEL keyword
del list1
# clear the list
list2=["Book","Pen","Pencil"]
list2.clear()
print(list2)
